// --------------------------------------------------------------------------
// 연습: 문자 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
console.log('자바스크립트 연습 중입니다.');
// --------------------------------------------------------------------------
// 연습: 논리 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
console.log(true);
// --------------------------------------------------------------------------
// 연습: 여러 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
console.log('유영', '연습중입니다.');
// --------------------------------------------------------------------------
// 연습: 콘솔 패널에 오류 또는 경고 출력
// --------------------------------------------------------------------------
console.error('🚫 문자 값은 따옴표로 묶어 출력하지 않으면 오류입니다.🚫');
console.warn('❗JavaScript의 데이터를 기억하려면 변수 또는 상수가 필요합니다.');
console.info('💡터미널 콘솔의 다양하게 활용할 수 있습니다.💡');
// --------------------------------------------------------------------------
// 연습: 콘솔 그룹화 출력
// --------------------------------------------------------------------------

// 숫자 (정수)
console.group('숫자 (정수)');
console.log(10);
console.log(101);
console.log(908);
console.groupEnd();

// 숫자 (소수)
console.group('숫자 (소수)');
console.log(0.6);
console.log(0.23);
console.log(0.964);
console.groupEnd();

